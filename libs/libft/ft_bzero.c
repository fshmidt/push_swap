#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			a;
	unsigned char	*d;

	a = 0;
	d = (unsigned char *)s;
	while (n - a)
	{
		d[a] = '\0';
		a++;
	}
}
